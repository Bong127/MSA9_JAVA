package Day02;

public class Ex04_Int {
	public static void main(String[] args) {
		// 10진수
		int var1 = 10;
		System.out.println("var1 : " + var1);
		
		// 8진수
		int var2 = 010;
		System.out.println("var2 : " + var2);
		
		// 16진수
		int var3 = 0x10;
		System.out.println("var3 : " + var3);
		
		// 2진수
		int var4 = 0b1100;
		System.out.println("var4 : " + var4);
	}
}
